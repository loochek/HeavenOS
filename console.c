bool cons_initialized = false;

static int cons_width = 0;
static int cons_height = 0;

static int cons_curr_row = 0;
static int cons_curr_col = 0;

static void cons_newline_and_return();
static void draw_char(char c, int row, int col, fb_color_t fg_color, fb_color_t bg_color);

void cons_init()
{
    int fb_width = 0, fb_height = 0;
    fb_get_size(&fb_width, &fb_height);

    cons_width  = fb_width  / FONT_WIDTH;
    cons_height = fb_height / FONT_HEIGHT;

    cons_initialized = true;
}

void cons_putchar_color(char c, fb_color_t fg_color, fb_color_t bg_color)
{
    kassert(cons_initialized);

    switch (c)
    {
    case '\n':
        cons_newline_and_return();
        break;

    case '\r':
        cons_curr_row = 0;
        break;

    default:
        draw_char(c, cons_curr_row, cons_curr_col, fg_color, bg_color);
        cons_curr_col++;
        if (cons_curr_col == cons_width)
            cons_newline_and_return();
    }
}

void cons_putchar(char c)
{
    kassert(cons_initialized);

    cons_putchar_color(c, FB_COLOR_WHITE, FB_COLOR_BLACK);
}

void cons_print_color(const char *str, fb_color_t fg_color, fb_color_t bg_color)
{
    kassert(cons_initialized);
    kassert_dbg(str != NULL);

    while (*str != '\0')
    {
        cons_putchar_color(*str, fg_color, bg_color);
        str++;
    }
}

void cons_print(const char *str)
{
    kassert(cons_initialized);
    kassert_dbg(str != NULL);

    cons_print_color(str, FB_COLOR_WHITE, FB_COLOR_BLACK);
}

void cons_write_color(const char *data, int size, fb_color_t fg_color, fb_color_t bg_color)
{
    kassert(cons_initialized);
    kassert_dbg(data != NULL);

    for (int i = 0; i < size; i++)
        cons_putchar_color(data[i], fg_color, bg_color);
}

void cons_write(const char *data, int size)
{
    kassert(cons_initialized);
    kassert_dbg(data != NULL);
    
    cons_write_color(data, size, FB_COLOR_WHITE, FB_COLOR_BLACK);
}

static void draw_char(char c, int row, int col, fb_color_t fg_color, fb_color_t bg_color)
{
    fb_draw_glyph(font[(unsigned int)c], FONT_WIDTH * col, FONT_HEIGHT * row, fg_color, bg_color);
}

static void cons_newline_and_return()
{
    cons_curr_col = 0;
    cons_curr_row++;
    if (cons_curr_row == cons_height)
    {
        cons_curr_row--;
        fb_scroll_up(FONT_HEIGHT, FB_COLOR_BLACK);
    }
}
